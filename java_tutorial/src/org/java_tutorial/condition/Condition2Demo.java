package org.java_tutorial.condition;

public class Condition2Demo {

	public static void main(String[] args) {
		 if (true) {
	            System.out.println(1);
	            System.out.println(2);
	            System.out.println(3);
	            System.out.println(4);
	        }
	        System.out.println(5);
	        if(false){
	            System.out.println(1);
	            System.out.println(2);
	            System.out.println(3);
	            System.out.println(4);
	        }
	        System.out.println(5);
	}

}
